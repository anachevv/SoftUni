import turtle

my_turtle = turtle.Turtle()
my_turtle.shape("turtle")

for x in range(5):
    my_turtle.forward(50)
    my_turtle.left(60)
    my_turtle.forward(70)
    my_turtle.left(60)
    my_turtle.forward(90)
    my_turtle.left(60)
    my_turtle.forward(110)
    my_turtle.left(60)

turtle.done()

